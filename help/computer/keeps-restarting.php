<h3>Windows 7</h3>
<p>
	<ol>
		<li>
		 Restart computer
		</li>
		<li>
		 Press f8 before the windows logo appears
		</li>
		<li>
		Choose safe mode from the Advanced Boot Options
		</li>
	</ol>
</p>
<h3>Windows 8, 8.1, 10</h3>
<p>
	<ol>
		<li>
		Hold Shift and restart computer
		</li>
		<li>
		Press Troubleshoot
		</li>
		<li>
		Choose Advanced Options
		</li>
		<li>
		Start-up Settings
		</li>
		<li>
		Restart
		</li>
		<li>
		Choose safe mode
		</li>
	</ol>
</p>
